package model;

/**
 *
 * @author ica
 */
public class UserBean {

    private int id;
    private String username;
    private String password;
    private String email;
    private Long phone;
    private String gender;
    private String firstname;
    private String lastname;
    private String address;
    private String country;
    private String answer;
    private String oldPassword;
    private String repassword;
    private int isAdmin;
    private boolean accStatus;

    public UserBean() {
    }
//Add Client
    public UserBean(String username, String password, String email, Long phone, String gender, String firstname, String lastname, String address, String country, String answer) {
        this.username = username;
        this.password = password;
        this.email = email;
        this.phone = phone;
        this.gender = gender;
        this.firstname = firstname;
        this.lastname = lastname;
        this.address = address;
        this.country = country;
        this.answer = answer;
        this.accStatus = true;
    }
//    Contructor to edit Profile
    public UserBean(String username, String email, Long phone, String gender, String firstname, String lastname, String address, String country) {
        this.username = username;
        this.email = email;
        this.phone = phone;
        this.gender = gender;
        this.firstname = firstname;
        this.lastname = lastname;
        this.address = address;
        this.country = country;
    }

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public String getEmail() {
        return email;
    }

    public Long getPhone() {
        return phone;
    }

    public String getGender() {
        return gender;
    }

    public String getFirstname() {
        return firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public String getAddress() {
        return address;
    }

    public String getCountry() {
        return country;
    }

    public String getAnswer() {
        return answer;
    }

    public String getOldPassword() {
        return oldPassword;
    }

    public int getIsAdmin() {
        return isAdmin;
    }

    public void setIsAdmin(int isAdmin) {
        this.isAdmin = isAdmin;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setPhone(Long phone) {
        this.phone = phone;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }

    public void setOldPassword(String oldPassword) {
        this.oldPassword = oldPassword;
    }

    public String getRepassword() {
        return repassword;
    }

    public void setRepassword(String repassword) {
        this.repassword = repassword;
    }

    public boolean isAccStatus() {
        return accStatus;
    }

    public void setAccStatus(boolean accStatus) {
        this.accStatus = accStatus;
    }

}
